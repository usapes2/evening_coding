#include <iostream>
using namespace std;

int main() {
	int i = 0;
	int j = 0;
	cout << i << endl ; //print
	cout << ++i << endl ; //Pre - increment
	cout << i << endl ; //print
	cout << " _______________________________ " <<endl;
	cout << j << endl ; //print
	cout << j++ << endl ; //Post - increment 
	cout << j << endl ; //print
	cout << endl;
	cout << endl;
	cout << endl;
	cout << endl;
	for (int i = 0 ; i < 5 ; ++i )
		cout << i << endl; 
	cout << endl;
	cout << endl;
	cout << endl;
	cout << endl;
	for (int i = 0 ; i < 5 ; i++ )
		cout << i << endl; 
	return 0;
}
